import { NextResponse } from "next/server"
import { buildScenes } from "@/lib/editor"

export const runtime = "nodejs"
export const dynamic = "force-dynamic"

export async function GET(_req: Request, { params }: { params: Promise<{ id: string }> }) {
  const { id } = await params
  const backendUrl = process.env.BACKEND_URL
  if (!backendUrl) {
    return NextResponse.json(
      { error: "Server misconfigured: BACKEND_URL is not set." },
      { status: 500 },
    )
  }

  let backendRes: Response
  try {
    backendRes = await fetch(`${backendUrl}/result/${id}`, { cache: "no-store" })
  } catch (e: unknown) {
    const message = e instanceof Error ? e.message : "unknown error"
    return NextResponse.json({ error: `Could not reach backend: ${message}` }, { status: 502 })
  }

  const data = await backendRes.json().catch(() => null)

  if (backendRes.status === 404) {
    return NextResponse.json({ error: "Run not found." }, { status: 404 })
  }
  if (!backendRes.ok) {
    return NextResponse.json(
      { error: data?.detail ?? `Backend returned ${backendRes.status}` },
      { status: backendRes.status },
    )
  }

  // Backend uses "complete" / "in_progress" / "queued" / "failed".
  // The frontend expects "done" / "running" / "error".
  const backendStatus: string = data?.status ?? "running"
  let frontendStatus: "running" | "done" | "error"
  if (backendStatus === "complete") {
    frontendStatus = "done"
  } else if (backendStatus === "failed") {
    frontendStatus = "error"
  } else {
    frontendStatus = "running"
  }

  if (frontendStatus !== "done") {
    return NextResponse.json({ id, status: frontendStatus, result: null })
  }

  // Map backend JobResult fields → RunResult shape expected by result-view.tsx
  //
  // Backend segment shape (from script_writer.py / app_browser.py):
  //   { segment_id, start_time, end_time, feature, talking_point,
  //     action, reason, observation, text, screen_note }
  // NOT: title / script / start / end
  const rawSegments: Array<Record<string, unknown>> =
    Array.isArray(data.segments) ? data.segments : []

  // The backend probes the glued final MP4, so its verified duration is the
  // source of truth. Segment timestamps exclude the title card and route-load
  // gaps, which made the UI under-report a correctly rendered 3/5-minute run.
  const lastSeg = rawSegments[rawSegments.length - 1]
  const durationSec: number =
    (typeof data.duration_seconds === "number" ? data.duration_seconds : null) ??
    (typeof lastSeg?.end_time === "number" ? lastSeg.end_time : 180)

  // Narration text — join all segment texts for the scriptPreview card.
  const scriptText: string =
    rawSegments
      .map((s) => (typeof s.text === "string" ? s.text.trim() : ""))
      .filter(Boolean)
      .join("\n\n") || ""

  const scenes =
    rawSegments.length > 0
      ? rawSegments.map((s, i) => ({
          id: `scene-${i}`,
          index: i,
          // backend stores the section name in "feature", narration in "text"
          title:
            typeof s.feature === "string" && s.feature.trim()
              ? s.feature.trim()
              : `Scene ${i + 1}`,
          startSec:
            typeof s.start_time === "number"
              ? s.start_time
              : (i * durationSec) / rawSegments.length,
          endSec:
            typeof s.end_time === "number"
              ? s.end_time
              : ((i + 1) * durationSec) / rawSegments.length,
          // the narration the AI generated for this segment
          script: typeof s.text === "string" ? s.text : "",
          status: "ready" as const,
        }))
      : buildScenes(scriptText || "Demo generated by Devsfield.", durationSec, "demo")

  // Use repo_name from models_used or fall back to "your app"
  const repoName: string =
    (typeof data.models_used?.repo_name === "string" && data.models_used.repo_name) ||
    (typeof data.repo_name === "string" && data.repo_name) ||
    "your app"

  // Build a minimal asset list from provenance fields the backend provides.
  const assets = [
    data.manifest_url && {
      key: `runs/${id}/manifest.json`,
      label: "Provenance manifest",
      kind: "json" as const,
      size: "—",
      b2Key: data.manifest_url,
      hash: (data.sha256 ?? "").slice(0, 8) || "—",
      provider: "Backblaze B2" as const,
      url: data.manifest_url,
    },
    data.video_url && {
      key: `runs/${id}/demo.mp4`,
      label: "Final demo video",
      kind: "video" as const,
      size: "—",
      b2Key: data.video_url,
      hash: (data.sha256 ?? "").slice(0, 8) || "—",
      provider: "Devsfield" as const,
      url: data.video_url,
    },
  ].filter(Boolean)

  // Ordered per-segment clip URLs. The player streams these back-to-back so
  // the viewer sees every segment (with its own voiceover) and no black
  // screens, even if the pre-rendered final_video.mp4 was short/silent.
  const clipUrls: string[] = rawSegments
    .slice()
    .sort((a, b) => {
      const ai = typeof a.segment_id === "number" ? a.segment_id : 0
      const bi = typeof b.segment_id === "number" ? b.segment_id : 0
      return ai - bi
    })
    .map((s) => (typeof s.clip_url === "string" ? s.clip_url : ""))
    .filter(Boolean)

  const result = {
    videoUrl: data.video_url ?? "",
    clipUrls,
    posterUrl: "/demo-poster.png",
    durationSec,
    title: `${repoName} — Demo`,
    summary: `An AI-generated walkthrough of ${repoName}.`,
    scriptPreview: scriptText,
    assets,
    scenes,
    version: 1,
    edits: [],
    format: "demo" as const,
    presenter: undefined,
  }

  return NextResponse.json({ id, status: frontendStatus, result })
}
